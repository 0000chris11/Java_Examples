package basic;


public class Basic_spacing {

      public static void main(String[] args) {

            System.out.println("KH\nIII\n");
            //skip a line
            System.out.println("Blank\tText\n");
            //skip tab
            System.out.println("GAME\rOVER\rGAME\r");
            //Send back to beginning of a line
            System.out.println("\"dabudida\"");

            //\f & \b can't be recognize
      }

}
