package Basic;


public class SwitchCaseBreak {

      public static void main(String[] args) {

            int valor = 3;

            switch (valor) {
                  case 0:
                        System.out.print("Mensaje 0: ");
                        break;
                  case 1:
                        System.out.print("Mensaje 1: ");
                        break;
                  case 2:
                        System.out.print("Mensaje 2: ");
                        break;
                  case 3:
                        System.out.print("Mensaje 3: ");
                        break;
                  default:
                        System.out.print("Mensaje 4: ");
                        break;
            }
      }
}
